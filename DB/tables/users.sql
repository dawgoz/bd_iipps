CREATE TABLE "USERS"
(
    "ID"           NUMBER(8, 0)
                      GENERATED BY DEFAULT ON NULL AS IDENTITY
                          MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH    1 CACHE 20 NOORDER NOCYCLE NOKEEP NOSCALE
                      NOT NULL ENABLE
   ,"USER_ROLE"    VARCHAR2(20) NOT NULL ENABLE
   ,"NAME"         VARCHAR2(20) NOT NULL ENABLE
   ,"LAST_NAME"    VARCHAR2(20) NOT NULL ENABLE
   ,"USER_NAME"    VARCHAR2(20) NOT NULL ENABLE
   ,"PASSWORD"     VARCHAR2(254) NOT NULL ENABLE
   ,"ACTIVE"       NUMBER(1, 0) NOT NULL ENABLE
   ,"ADDRESS"      VARCHAR2(50)
   ,"EMAIL"        VARCHAR2(50)
   ,CONSTRAINT "USERS_PK" PRIMARY KEY("ID") USING INDEX ENABLE
   ,CONSTRAINT "USERS_UK1" UNIQUE("USER_NAME") USING INDEX ENABLE
);

CREATE UNIQUE INDEX "USERS_PK"
    ON "USERS"("ID");

CREATE UNIQUE INDEX "USERS_UK1"
    ON "USERS"("USER_NAME");